package com.company.methods;

public class MethodOverloading {

    static int myMethod(int x) {
        return x - 1;
    }

    static float myMethod(float x) {
        return x + 1;
    }

    static double myMethod(double x, double y) {
        return x + y;
    }

    public static void main(String[] args) {
        System.out.println();
    }

}
